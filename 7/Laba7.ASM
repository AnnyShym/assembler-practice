use16			     ; генерировать 16-битный код
org 100h		     ; программа начинается с адреса 100h



	; ДАННАЯ ПРОГРАММА РИСУЕТ ФЛАГ НОРВЕГИИ С ИСПОЛЬЗОВАНИЕМ ФУНКЦИЙ BIOS



	mov аh, 00h
	mov al, 13h	     ; графический режим 320x200, 256 цветов
	int 10h


;==================================== вывод фона ====================================


	mov ah, 0Ch	     ; функция для вывода пикселя на экран
	mov al, 00000100b    ; красный цвет
	mov cx, 0	     ; начальное значение x
	mov dx, 0	     ; начальное значение y


	; прорисовка 200 строк
	width:		     
		inc dx	     ; приращение y
		mov cx, 0    ; начальное значение x


	; прорисовка строки длиной 300 пикселей
	length: 	     
		inc cx	     ; рисуем следующий пиксель
		int 10h


	cmp cx, 300
	jl length	     ; если меньше, рисуем следующий пиксель строки


	cmp dx, 200
	jl width	     ; если меньше, рисуем строку на пиксель ниже


;========================== вывод белой вертикальной линии ==========================


	mov al, 00001111b    ; белый цвет
	mov cx, 80	     ; начальное значение x
	mov dx, 0	     ; начальное значение y


	; прорисовка 200 строк
	wh_vert_width:
		inc dx	     ; приращение y
		mov cx, 80   ; начальное значение x


	; прорисовка строки длиной 40 пикселей
	wh_vert_length:
		inc cx	     ; рисуем следующий пиксель
		int 10h


	cmp cx, 120
	jl wh_vert_length    ; если меньше, рисуем следующий пиксель строки


	cmp dx, 200
	jl wh_vert_width     ; если меньше, рисуем строку на пиксель ниже


;========================= вывод белой горизонтальной линии =========================


	mov cx, 0	     ; начальное значение x
	mov dx, 80	     ; начальное значение y


	; прорисовка 40 строк
	wh_horiz_width:
		inc dx	     ; приращение y
		mov cx, 0    ; начальное значение x


	; прорисовка строки длиной 300 пикселей
	wh_horiz_length:
		inc cx	     ; рисуем следующий пиксель
		int 10h


	cmp cx, 300
	jl wh_horiz_length   ; если меньше, рисуем следующий пиксель строки


	cmp dx, 120
	jl wh_horiz_width    ; если меньше, рисуем строку на пиксель ниже


;========================== вывод синей вертикальной линии ==========================


	mov al, 00000001b    ; синий цвет
	mov cx, 90	     ; начальное значение x
	mov dx, 0	     ; начальное значение y


	; прорисовка 200 строк
	bl_vert_width:
		inc dx	     ; приращение y
		mov cx, 90   ; начальное значение x


	; прорисовка строки длиной 20 пикселей
	bl_vert_length:
		inc cx	     ; рисуем следующий пиксель
		int 10h


	cmp cx, 110
	jl bl_vert_length    ; если меньше, рисуем следующий пиксель строки


	cmp dx, 200
	jl bl_vert_width     ; если меньше, рисуем строку на пиксель ниже


;========================= вывод синей горизонтальной линии =========================


	mov cx, 0	     ; начальное значение x
	mov dx, 90	     ; начальное значение y


	; прорисовка 20 строк
	bl_horiz_width:
		inc dx	     ; приращение y
		mov cx, 0    ; начальное значение x


	; прорисовка строки длиной 300 пикселей
	bl_horiz_length:
		inc cx	     ; рисуем следующий пиксель
		int 10h


	cmp cx, 300
	jl bl_horiz_length   ; если меньше, рисуем следующий пиксель строки


	cmp dx, 110
	jl bl_horiz_width    ; если меньше, рисуем строку на пиксель ниже


;=============================== завершение программы ===============================


	mov ah, 00h	     ;\
	int 16h 	     ;/ ожидаем нажатия на любую клавишу

 
	mov ax, 4C00h	     ;\
	int 21h 	     ;/ завершение программы